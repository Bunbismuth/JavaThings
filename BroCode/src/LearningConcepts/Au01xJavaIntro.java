package LearningConcepts;

public class Au01xJavaIntro {
    // Qualquer programa java nunca irá rodar sem o método main.
    public static void main(String[] args){
        System.out.print("\"Eu amo pizza\"\n");
        System.out.println("\t\\É muito bom!");
    }

    // Caractéres de Escape
    // \n para uma nova linha
    // \t para espaço de tabulação
    // \" para colocar aspas duplas dentro de prints
    // \\ para mostrar uma barra invertida

    /*
    Comentário de várias linhas em Java.
     */
}
