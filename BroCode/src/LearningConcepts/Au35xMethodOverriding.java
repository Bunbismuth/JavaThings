package LearningConcepts;

public class Au35xMethodOverriding {
    public static void main(String[] args) {
        // Overrinding de método = declarar um método em uma subclasse na qual já se está presente na classe pai.
        // É feito para que a classe filha possa colocar suas próprias implementações dos métodos.

        Au35y2xDog dog = new Au35y2xDog();

        dog.speak();
    }
}
